# OP LOGICOS

p = True
q = False

# NOT
print(p)
print(f'Negado: {not(p)}')
print(not(0)) # 0 = False
print(not(1)) # 1 ou >1 = True
print(~q)
print(not(True))
print(not(False))
print()

# AND - CONJUNCAO
print(True and True)
print(True and False)
print(False and False)
print(True & True)
print()

# OR - DISJUNCAO
print(True or True)
print(True or False)
print(False or False)
print(True | True)
print()
